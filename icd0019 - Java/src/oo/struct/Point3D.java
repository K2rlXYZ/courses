package oo.struct;

public class Point3D {
    public int x, y, z;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }

    public Point3D(int inX, int inY, int inZ) {
        x = inX;
        y = inY;
        z = inZ;
    }
}
